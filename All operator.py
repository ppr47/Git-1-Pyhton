x=10
y=5

print("x=",x)
print("y=",y)


#Logical
print(x>5 and x>9)
print(y>5 and y>9)
print(x>6 or y>9)


#Arithmetic
print("Addition is=",x+y)
print("Substraction is=",x-y)
print("multiplication is=",x*y)
print("Division is=",x/y)
print("Floor Division is=",x//y)
print("Modulus is=",x%y)

#Comparision
print("Is x==y:",x==y)
print("Is x<y:",x<y)
print("Is x>y:",x>y)
print("Is x!=y:",x!=y)
print("Is x<=y:",x<=y)
print("Is x>=y:",x>=y)

#Identity
print("x=y:",x is y)
print("x!=y:",x is not y)